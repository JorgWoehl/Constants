		% constructor function
		function obj = Constants(prop, year)
			arguments
				prop (1,:) char {mustBeMember(prop, {'id', 'year', 'value', 'uncty', 'unit', 'name', 'isExact', 'isIrr', 'symvalue', 'symunit', 'sym', 'all'})} = 'value'
				year (1,:) char {mustBeMember(year, {'1998', '2002', '2006', '2010', '2014', '2018', '2022', 'latest', 'all'})} = 'latest'
			end

			varNames = fieldnames(obj);                 % list of variable names
			numPattern = '(\d+(\.\d*)?(e[+-]?\d+)?)';	% matches a number (engineering notation) without the sign

			% check if the Symbolic Math Toolbox is accessible
			hasSymbolicMath = true;
			try
				sym(1/3);
			catch
				hasSymbolicMath = false;
			end

			if hasSymbolicMath && (contains(prop, 'sym') || strcmp(prop, 'all'))
				% add new unit C_90, which appears in 'Fstar' (definition is temporary for now)
				warning('off', 'symbolic:unit:UnitChangeDefinition');
				newUnit('C_90', '1');

				% add properties 'symvalue', 'symunit', 'sym'
				for i = 1:length(varNames)
					internalVar = [varNames{i} '_'];

					% symvalue: make all unsigned numbers in 'value' symbolic
					symval = regexprep(obj.(internalVar).value, numPattern, "sym('$1')");
					% make pi symbolic
					symval = replace(symval, 'pi', 'sym(pi)');
					% make NaN symbolic
					symval = replace(symval, 'NaN', "sym('NaN')");
					obj.(internalVar).symvalue = str2sym(symval);

					% symunit: replace all spaces in 'unit' by multiplication signs
					symun = replace(obj.(internalVar).unit, ' ', '*');
					% replace all "C_{90}" in symun by "C_90"
					symun = replace(symun, 'C_{90}', 'C_90');
					% replace all curly braces by parentheses
					symun = replace(symun, '{', '(');
					symun = replace(symun, '}', ')');
					% replace "" by "1"
					symun(symun == "") = "1";
					obj.(internalVar).symunit = str2symunit(symun);
	
					% sym: combine symvalue and symunit
					obj.(internalVar).sym = obj.(internalVar).symvalue .* obj.(internalVar).symunit;
				end

				% redefine unit C_90 (properly, now that all needed variables are defined)
				removeUnit(symunit('C_90'));
				newUnit('C_90', obj.KJninety_.sym(end)*obj.RKninety_.sym(end)/(obj.KJ_.sym(end)*obj.RK_.sym(end)) * symunit('C'));
			end

			% bootstrap all other information
			internalVar = [varNames{1} '_'];            % use first internal variable as reference
			propNames = fieldnames(obj.(internalVar));  % list of property names for each variable

			% list of CODATA release years
			yearList = obj.(internalVar).year;
			% determine 'latest' year
			if strcmp(year, 'latest')
				year = num2str(max(yearList));
			end

			% parse 'year' argument
			if strcmp(year, 'all')
				obj.yearInd_ = 1:length(yearList);
			else
				obj.yearInd_ = find(strcmp(string(yearList), year));
			end

			% parse 'prop' argument
			if contains(prop, 'sym') && ~hasSymbolicMath
				error("The '" + prop + "' property requires the Symbolic Math Toolbox, which is not installed.");
			end

			if ~strcmp(prop, 'all')
				propNames = {prop};
			end

			% fallback values if lambertw function is unavailable because the Symbolic Math Toolbox is not present
			numberValue.b = ["2.8977686e-3", "2.8977686e-3", "2.8977685e-3", "2.8977721e-3", "2.8977729e-3", "2.89777195518517266147860544809288472680200737583337641455180616184266022326129663098483677072819568288458568146589445567649774835673412262732100948095426983731442331338879554198606489372604120585299083e-3", "2.89777195518517266147860544809288472680200737583337641455180616184266022326129663098483677072819568288458568146589445567649774835673412262732100948095426983731442331338879554198606489372604120585299083e-3"];
			numberValue.bprime = ["NaN", "NaN", "5.878933e10", "5.8789254e10", "5.8789238e10", "5.87892575764682494660613079530972169147435482460315617976960791148538774072174381481769379261988220856717721933800630503427612896757493717729065874740596894229651412795760759969720620892275896835745282e10", "5.87892575764682494660613079530972169147435482460315617976960791148538774072174381481769379261988220856717721933800630503427612896757493717729065874740596894229651412795760759969720620892275896835745282e10"];
			
			for i = 1:length(varNames)
				for j = 1:length(propNames)
					% copy properties from internal (private) to public variable
					internalVar = [varNames{i} '_'];

					% skip if all selected values are "NaN"
					if all(obj.(internalVar).value(obj.yearInd_) == "NaN")
						continue
					end

					if strcmp(propNames{j}, 'id')
						% 'id' is a scalar
						val = obj.(internalVar).(propNames{j});
						if isscalar(propNames)
							obj.(varNames{i}) = val;
						else
							obj.(varNames{i}).(propNames{j}) = val;
						end
					else
						% all other properties are 1xN arrays
						arr = obj.(internalVar).(propNames{j});
						if ~hasSymbolicMath && strcmp(propNames{j}, 'value')
							if any(strcmp(varNames{i}, fieldnames(numberValue)))
								arr = numberValue.(varNames{i});
							end
						end
						if strcmp(propNames{j}, 'value') || strcmp(propNames{j}, 'uncty')
							expr = regexprep(arr(obj.yearInd_), numPattern, "str2double('$1')"); 
							% loop needed because 'eval' expects a string scalar, not an array
							for m = 1:length(expr)
								val(m) = eval(expr(m));
							end
						else
							val = arr(obj.yearInd_);
						end
						if isscalar(propNames)
							obj.(varNames{i}) = val;
						else
							obj.(varNames{i}).(propNames{j}) = val;
						end
					end
				end
			end
		end
	
		function T = info(obj)
			% Return list of constants, units, and variables in table format

			varNames = fieldnames(obj);                 % list of variable names
			n = 0;										% counter
			for i = 1:length(varNames)
				if ~isempty(obj.(varNames{i}))
					n = n+1;							% new table element
					internalVar = [varNames{i} '_'];
		
					% variable name
					Tvar{n,1} = varNames{i};								%#ok<AGROW>

					% current name of the constant, starting with uppercase character
					s = obj.(internalVar).id;
					s(1) = upper(s(1));
					Tid(n,1) = string(s);									%#ok<AGROW>

					% find unit of most recent non-NaN value
					m = length(obj.yearInd_);
					while (m >= 1) && (obj.(internalVar).value(obj.yearInd_(m)) == "NaN")
						m = m-1;
					end
					Tunit(n,1) = obj.(internalVar).unit(obj.yearInd_(m));	%#ok<AGROW>
					if strcmp(Tunit(n,1), "")
						Tunit(n,1) = "(unitless)";							%#ok<AGROW>
					end
				end
			end

			% create table
			T = table(char(Tvar), char(Tid), char(Tunit),...
				'VariableNames', {'Variable', 'Constant', 'Unit'});

			% sort alphabetically by 'Constant' column
			T = sortrows(T,2);
		end
	
		function T = find(obj, s)
			% Return all constants containing the search string in table format (case-insensitive)

			T = obj.info;
			rows = contains(lower(string(T.Constant)), lower(s));
			T = T(rows, :);

			if isempty(T)
				T = table.empty;
			else
				% remove unnecessary whitespace from table columns
				T.Variable = char(cellstr(T.Variable));
				T.Constant = char(cellstr(T.Constant));
				T.Unit = char(cellstr(T.Unit));
			end
		end
	
